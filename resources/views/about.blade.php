    <script>
        document.addEventListener('DOMContentLoaded', function() {
            // Country Tabs
            const tabs = document.querySelectorAll('.country-tab');
            
            tabs.forEach(tab => {
                tab.addEventListener('click', function() {
                    // Remove active class from all tabs
                    tabs.forEach(t => t.classList.remove('active'));
                    
                    // Add active class to clicked tab
                    this.classList.add('active');
                    
                    // Hide all content
                    const contents = document.querySelectorAll('.country-content');
                    contents.forEach(content => content.classList.remove('active'));
                    
                    // Show selected content
                    const country = this.getAttribute('data-country');
                    document.getElementById(`${country}-content`).classList.add('active');
                });
            });
            
            // FAQ Accordion
            const faqQuestions = document.querySelectorAll('.faq-question');
            
            faqQuestions.forEach(question => {
                question.addEventListener('click', function() {
                    const answer = this.nextElementSibling;
                    const isActive = this.classList.contains('active');
                    
                    // Close all other answers
                    document.querySelectorAll('.faq-question').forEach(q => {
                        q.classList.remove('active');
                        q.nextElementSibling.classList.remove('active');
                    });
                    
                    // Toggle current answer
                    if (!isActive) {
                        this.classList.add('active');
                        answer.classList.add('active');
                    }
                });
            });
            
            // Intersection Observer for animations
            const animatedElements = document.querySelectorAll('.animate-fade-up, .animate-fade-left, .animate-fade-right');
            
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('animate-visible');
                        observer.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            });
            
            animatedElements.forEach(element => {
                observer.observe(element);
            });
            
            // Number Counter Animation
            const statsNumbers = document.querySelectorAll('.stats-number');
            
            const statsObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const target = entry.target;
                        const countTo = parseInt(target.getAttribute('data-count'));
                        const currentText = target.textContent;
                        
                        // Skip if it's already animated or has a plus sign
                        if (currentText.includes('+') || currentText.includes('%')) {
                            statsObserver.unobserve(target);
                            return;
                        }
                        
                        let count = 0;
                        const duration = 2000; // 2 seconds
                        const interval = 50; // update every 50ms
                        const steps = duration / interval;
                        const increment = countTo / steps;
                        
                        const counter = setInterval(() => {
                            count += increment;
                            if (count >= countTo) {
                                clearInterval(counter);
                                count = countTo;
                                
                                // Add + or % if present in the original text
                                if (currentText.includes('+')) {
                                    target.textContent = Math.floor(count) + '+';
                                } else if (currentText.includes('%')) {
                                    target.textContent = Math.floor(count) + '%';
                                } else {
                                    target.textContent = Math.floor(count);
                                }
                            } else {
                                target.textContent = Math.floor(count);
                            }
                        }, interval);
                        
                        statsObserver.unobserve(target);
                    }
                });
            }, {
                threshold: 0.5
            });
            
            statsNumbers.forEach(number => {
                statsObserver.observe(number);
            });
        });
    </script>
