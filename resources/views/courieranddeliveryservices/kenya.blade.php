    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const buttons = document.querySelectorAll('.faq-accordion .btn-link');
            
            buttons.forEach(button => {
                button.addEventListener('click', () => {
                    const collapse = button.closest('.card').querySelector('.collapse');
                    const isExpanded = button.getAttribute('aria-expanded') === 'true';
                    
                    // Close all other open accordions
                    document.querySelectorAll('.faq-accordion .collapse.show').forEach(openCollapse => {
                        if (openCollapse !== collapse) {
                            openCollapse.classList.remove('show');
                            openCollapse.closest('.card').querySelector('.btn-link').setAttribute('aria-expanded', 'false');
                        }
                    });
                    
                    // Toggle the clicked accordion
                    collapse.classList.toggle('show');
                    button.setAttribute('aria-expanded', !isExpanded);
                });
            });
        });
    </script>

