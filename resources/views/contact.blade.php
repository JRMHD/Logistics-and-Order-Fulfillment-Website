    <script>
        // Animation on scroll
        document.addEventListener('DOMContentLoaded', function() {
            // Animate elements when they come into view
            const animateElements = document.querySelectorAll('[data-animation]');
            
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const element = entry.target;
                        const animation = element.getAttribute('data-animation');
                        const delay = element.getAttribute('data-delay') || 0;
                        
                        setTimeout(() => {
                            element.classList.add('visible');
                            element.classList.add(animation);
                        }, delay);
                        
                        observer.unobserve(element);
                    }
                });
            }, {
                threshold: 0.1
            });
            
            animateElements.forEach(element => {
                observer.observe(element);
            });
            
            // Contact form submission
            $("#contactForm").on("submit", function(e) {
                e.preventDefault();
                
                $("#submitBtn").prop("disabled", true);
                $("#loading").show();
                $("#responseMessage").html('');
                
                $.ajax({
                    url: "{{ route('contact.store') }}",
                    method: "POST",
                    data: $(this).serialize(),
                    success: function(response) {
                        $("#loading").hide();
                        $("#submitBtn").prop("disabled", false);
                        $("#responseMessage").html('<div class="success-message">' + response.message + '</div>');
                        $("#contactForm")[0].reset();
                    },
                    error: function(xhr) {
                        $("#loading").hide();
                        $("#submitBtn").prop("disabled", false);
                        let errors = xhr.responseJSON.errors;
                        let errorMessage = '<div class="error-message">';
                        $.each(errors, function(key, value) {
                            errorMessage += value[0] + '<br>';
                        });
                        errorMessage += '</div>';
                        $("#responseMessage").html(errorMessage);
                    }
                });
            });
        });
        
        // Scroll to contact form
        function scrollToContactForm() {
            const contactForm = document.getElementById('contact-form');
            contactForm.scrollIntoView({ behavior: 'smooth' });
        }
        
        // FAQ Toggle
        function toggleFaq(element) {
            // Toggle active class on question
            element.classList.toggle('active');
            
            // Toggle answer visibility
            const answer = element.nextElementSibling;
            answer.classList.toggle('active');
            
            // Close other open FAQs
            const allFaqs = document.querySelectorAll('.faq-question');
            allFaqs.forEach(faq => {
                if (faq !== element && faq.classList.contains('active')) {
                    faq.classList.remove('active');
                    faq.nextElementSibling.classList.remove('active');
                }
            });
        }
        
        // Open first FAQ by default
        document.addEventListener('DOMContentLoaded', function() {
            const firstFaq = document.querySelector('.faq-question');
            if (firstFaq) {
                toggleFaq(firstFaq);
            }
        });
