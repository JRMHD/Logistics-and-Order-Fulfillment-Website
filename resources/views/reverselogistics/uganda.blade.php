    <script>
        // Animate elements when they come into view
        function animateOnScroll() {
            const elements = document.querySelectorAll('.animate-on-scroll');
            
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        if (entry.target.classList.contains('fade-in')) {
                            // Already has animation class
                        } else {
                            entry.target.classList.add('fade-in');
                        }
                        observer.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.1
            });
            
            elements.forEach(element => {
                observer.observe(element);
            });
        }
        
        // FAQ Accordion functionality
        function setupFAQ() {
            const faqItems = document.querySelectorAll('.faq-item');
            
            faqItems.forEach(item => {
                const question = item.querySelector('.faq-question');
                
                question.addEventListener('click', () => {
                    const isActive = item.classList.contains('active');
                    
                    // Close all items
                    faqItems.forEach(faqItem => {
                        faqItem.classList.remove('active');
                    });
                    
                    // Open clicked item if it wasn't already open
                    if (!isActive) {
                        item.classList.add('active');
                    }
                });
            });
        }
        
        // Counter animation
        function animateCounters() {
            const counters = document.querySelectorAll('.counter-animation');
            
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        const target = entry.target;
                        const value = target.innerText;
                        
                        // Only animate if it's a number or percentage
                        if (value.match(/[\d.]+%?/)) {
                            let startValue = 0;
                            const endValue = parseFloat(value);
                            const duration = 2000;
                            const isPercentage = value.includes('%');
                            const hasDecimal = value.includes('.');
                            const decimalPlaces = hasDecimal ? value.split('.')[1].length : 0;
                            
                            let counter = setInterval(() => {
                                startValue += endValue / 20;
                                
                                if (startValue >= endValue) {
                                    target.innerText = value;
                                    clearInterval(counter);
                                } else {
                                    target.innerText = hasDecimal 
                                        ? startValue.toFixed(decimalPlaces) + (isPercentage ? '%' : '')
                                        : Math.floor(startValue) + (isPercentage ? '%' : '');
                                }
                            }, duration / 20);
                        }
                        
                        observer.unobserve(target);
                    }
                });
            }, {
                threshold: 0.5
            });
            
            counters.forEach(counter => {
                observer.observe(counter);
            });
        }
        
        // Initialize all functions when DOM is loaded
        document.addEventListener('DOMContentLoaded', function() {
            animateOnScroll();
            setupFAQ();
            animateCounters();
            
            // Open first FAQ item by default
            const firstFAQ = document.querySelector('.faq-item');
            if (firstFAQ) {
                firstFAQ.classList.add('active');
            }
        });
    </script>
