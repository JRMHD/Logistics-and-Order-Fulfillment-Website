    <script>
        function toggleFaq(element) {
            // Toggle active class on the question
            element.classList.toggle('active');
            
            // Find the answer element (next sibling)
            var answer = element.nextElementSibling;
            
            // Toggle show class on the answer
            answer.classList.toggle('show');
            
            // Close other open FAQs
            var allQuestions = document.querySelectorAll('.faq-question');
            var allAnswers = document.querySelectorAll('.faq-answer');
            
            for (var i = 0; i < allQuestions.length; i++) {
                if (allQuestions[i] !== element && allQuestions[i].classList.contains('active')) {
                    allQuestions[i].classList.remove('active');
                    allAnswers[i].classList.remove('show');
                }
            }
        }
    </script>
